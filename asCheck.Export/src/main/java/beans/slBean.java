package beans;

public class slBean {
	String slCode;
	String slName;
	String slEmCode;
	String slCrCode;
	String slStartDate;
	String slEndDate;
	public String getSlCode() {
		return slCode;
	}
	public void setSlCode(String slCode) {
		this.slCode = slCode;
	}
	public String getSlName() {
		return slName;
	}
	public void setSlName(String slName) {
		this.slName = slName;
	}
	public String getSlEmCode() {
		return slEmCode;
	}
	public void setSlEmCode(String slEmCode) {
		this.slEmCode = slEmCode;
	}
	public String getSlCrCode() {
		return slCrCode;
	}
	public void setSlCrCode(String slCrCode) {
		this.slCrCode = slCrCode;
	}
	public String getSlStartDate() {
		return slStartDate;
	}
	public void setSlStartDate(String slStartDate) {
		this.slStartDate = slStartDate;
	}
	public String getSlEndDate() {
		return slEndDate;
	}
	public void setSlEndDate(String slEndDate) {
		this.slEndDate = slEndDate;
	}
	
}
