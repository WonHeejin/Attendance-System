package beans;

public class THBean {
	String thDate;
	String thName;
	String thStName;
	public String getThDate() {
		return thDate;
	}
	public void setThDate(String thDate) {
		this.thDate = thDate;
	}
	public String getThName() {
		return thName;
	}
	public void setThName(String thName) {
		this.thName = thName;
	}
	public String getThStName() {
		return thStName;
	}
	public void setThStName(String thStName) {
		this.thStName = thStName;
	}
	
	
}
